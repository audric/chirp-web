const SUPPORTED_LANGS = Object.keys(translations);
const DEFAULT_LANG = "en";

function getPreferredLang() {
    const saved = localStorage.getItem("chirpweb-lang");
    if (saved && SUPPORTED_LANGS.includes(saved)) return saved;
    // Check browser language
    const browserLang = (navigator.language || "").slice(0, 2).toLowerCase();
    if (SUPPORTED_LANGS.includes(browserLang)) return browserLang;
    return DEFAULT_LANG;
}

let currentLang = getPreferredLang();

function t(key, params) {
    let str = (translations[currentLang] && translations[currentLang][key])
        || translations[DEFAULT_LANG][key]
        || key;
    if (params) {
        for (const [k, v] of Object.entries(params)) {
            str = str.replace(`{${k}}`, v);
        }
    }
    return str;
}

function applyTranslations() {
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        el.innerHTML = t(key);
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
        const key = el.getAttribute("data-i18n-placeholder");
        el.placeholder = t(key);
    });
    // Update html lang attribute
    document.documentElement.lang = currentLang;
    // Update active state on language switcher
    document.querySelectorAll(".lang-btn").forEach(btn => {
        btn.classList.toggle("active", btn.dataset.lang === currentLang);
    });
}

function setLang(lang) {
    if (!SUPPORTED_LANGS.includes(lang)) return;
    currentLang = lang;
    localStorage.setItem("chirpweb-lang", lang);
    applyTranslations();
}

// Initialize on DOM ready
document.addEventListener("DOMContentLoaded", () => {
    applyTranslations();
});
